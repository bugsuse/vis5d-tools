CEXT *******************************************************************
C     Funzioni trigonometriche esterne per coord. espresse in gradi
C     (centesimali).
CEXT *******************************************************************
CEXT ===================================================================
      function SIND(AD)
CEXT ===================================================================

      parameter (PI=3.141592654)
      parameter (CV=PI/180.)
      SIND=SIN(AD*CV)
      return
      end

CEXT ==================================================================
      function COSD(AD)
CEXT ==================================================================

      parameter (PI=3.141592654)
      parameter (CV=PI/180.)
      COSD=COS(AD*CV)
      return
      end

CEXT ==================================================================
      function TAND(AD)
CEXT ==================================================================

      parameter (PI=3.141592654)
      parameter (CV=PI/180.)
      TAND=TAN(AD*CV)
      return
      end

CEXT ==================================================================
      function ASIND(S)
CEXT ==================================================================

      parameter (PI=3.141592654)
      parameter (VC=180./PI)
      ASIND=ASIN(S)*VC
      return
      end

CEXT ==================================================================
      function ACOSD(S)
CEXT ==================================================================

      parameter (PI=3.141592654)
      parameter (VC=180./PI)
      ACOSD=ACOS(S)*VC
      return
      end

CEXT ==================================================================
      function ATAND(T)
CEXT ==================================================================

      parameter (PI=3.141592654)
      parameter (VC=180./PI)
      ATAND=ATAN(T)*VC
      return
      end

CEXT =================================================================
      function atan2d(arc,orc)
CEXT ==================================================================
      parameter (pi=3.141592654)
      atan2d=180.*atan2(arc,orc)/pi
      return
      end
